// Helper to get exercise by ID
export function getExerciseById(id: string): Exercise | undefined {
  return exercises.find(e => e.id === id);
}

// Helper to get exercises by muscle group
export function getExercisesByMuscle(muscleGroup: string): Exercise[] {
  return exercises.filter(e =>
    e.muscleGroups.some(mg => mg.toLowerCase().includes(muscleGroup.toLowerCase()))
  );
}
